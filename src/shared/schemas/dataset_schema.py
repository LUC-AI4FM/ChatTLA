"""
dataset_schema.py — Canonical data record definition for ChatTLA.

Every record that flows through the scraper → validator → training pipeline
must conform to this schema.  The dataclass serves as documentation AND as
a runtime validation point (via `DatasetRecord.validate()`).

Schema design notes
-------------------
- `validation_tier` mirrors TLC's capability spectrum:
    gold   — TLC model-checks the spec without errors
    silver — SANY parses the spec (syntactically valid TLA+)
    bronze — Neither; kept in `data/rejected/` for analysis only
- `source` uses a URI-style prefix so we can tell at a glance where a spec
  came from (github:owner/repo/path, formalllm:id, synthetic:strategy).
- `annotation` is populated by the local Ollama gpt-oss:20b pass (Phase 1,
  `src/scraper/annotate.py`).  We do NOT call GPT-4o to avoid costs.
- `harmony_messages` holds the pre-formatted harmony message list ready to
  feed directly into SFTTrainer — built by `src/training/dataset_builder.py`.
"""

from __future__ import annotations

import hashlib
from dataclasses import dataclass, field, asdict
from typing import Literal, Optional
import json


ValidationTier = Literal["gold", "silver", "bronze"]
Domain = Literal[
    "consensus", "storage", "networking", "security",
    "hardware", "transaction", "scheduling", "other"
]


@dataclass
class TLCResult:
    """Raw output from a TLC model-check run."""
    tier: ValidationTier
    sany_errors: list[str] = field(default_factory=list)
    tlc_errors: list[str] = field(default_factory=list)
    tlc_output: str = ""
    runtime_seconds: float = 0.0


@dataclass
class QualityScore:
    """Heuristic quality signal for a TLA+ spec (1–5 scale)."""
    overall: int = 0          # 1 (trivial) – 5 (rich, documented, non-trivial)
    line_count: int = 0
    has_comments: bool = False
    has_invariants: bool = False
    has_liveness: bool = False
    operator_richness: int = 0  # count of distinct TLA+ operators used


@dataclass
class Annotation:
    """
    Natural-language annotations generated by local Ollama gpt-oss:20b.
    No GPT-4o calls — keeps the pipeline fully air-gapped and free.
    """
    natural_language_description: str = ""
    domain: Optional[Domain] = None
    difficulty: int = 0         # 1 (beginner) – 5 (research-grade)
    key_invariants: list[str] = field(default_factory=list)
    key_design_decisions: list[str] = field(default_factory=list)


@dataclass
class HarmonyMessage:
    """A single message in gpt-oss harmony format."""
    role: Literal["developer", "user", "assistant"]
    channel: Optional[Literal["analysis", "commentary", "final"]] = None
    content: str = ""


@dataclass
class DatasetRecord:
    """
    One TLA+ training record.  Populated incrementally as it moves through
    ingestion → validation → annotation → harmony-formatting.
    """
    # --- Identity -----------------------------------------------------------
    id: str = ""                # SHA-256 of normalised tla_content
    source: str = ""            # e.g. "github:tlaplus/examples/Paxos"
    license: str = "unknown"    # SPDX identifier, e.g. "MIT", "Apache-2.0"

    # --- Raw content --------------------------------------------------------
    tla_content: str = ""       # Full .tla file text
    cfg_content: Optional[str] = None  # .cfg file text; None if absent

    # --- Validation ---------------------------------------------------------
    tlc_result: Optional[TLCResult] = None
    quality: Optional[QualityScore] = None

    # --- Annotation (Phase 1, post-validation) ------------------------------
    annotation: Optional[Annotation] = None

    # --- Training-ready (Phase 2, dataset_builder.py) -----------------------
    # Each task type becomes its own list of harmony messages.
    # Keys: "spec_generation", "spec_completion", "invariant_gen", "bug_fix"
    harmony_messages: dict[str, list[HarmonyMessage]] = field(default_factory=dict)

    # --- Metadata -----------------------------------------------------------
    metadata: dict = field(default_factory=dict)
    # Typical metadata keys:
    #   module_name, extends[], line_count, stars, last_commit, repo_url

    # -----------------------------------------------------------------------

    @classmethod
    def make_id(cls, tla_content: str) -> str:
        """Stable content-addressed ID: SHA-256 of normalised spec text."""
        normalised = tla_content.strip().encode("utf-8")
        return hashlib.sha256(normalised).hexdigest()

    def validate(self) -> list[str]:
        """
        Lightweight runtime validation.
        Returns a list of error strings (empty = valid).
        """
        errors: list[str] = []
        if not self.id:
            errors.append("id is empty")
        if not self.tla_content:
            errors.append("tla_content is empty")
        if not self.source:
            errors.append("source is empty")
        if self.id != self.make_id(self.tla_content):
            errors.append("id does not match SHA-256 of tla_content")
        return errors

    def to_dict(self) -> dict:
        return asdict(self)

    def to_json(self, indent: int = 2) -> str:
        return json.dumps(self.to_dict(), indent=indent, ensure_ascii=False)

    @classmethod
    def from_dict(cls, d: dict) -> "DatasetRecord":
        """Reconstruct a DatasetRecord from a plain dict (e.g. loaded from JSONL)."""
        r = cls()
        for k, v in d.items():
            if k == "tlc_result" and v is not None:
                v = TLCResult(**v)
            elif k == "quality" and v is not None:
                v = QualityScore(**v)
            elif k == "annotation" and v is not None:
                v = Annotation(**v)
            elif k == "harmony_messages" and v is not None:
                v = {
                    task: [HarmonyMessage(**m) for m in msgs]
                    for task, msgs in v.items()
                }
            setattr(r, k, v)
        return r
